        <h1>Cube Bouldergym</h1>
        <h2>Boulderen is booming!</h2>
        <h3>In 2011 waren we de 2e boulderhal van Nederland, klein en knus. In de jaren daarna hebben we keer op keer een kleine uitbreiding gedaan in de zomer van 2017 hebben we onze boulderwanden flink uitgebreid, en begin 2018 hebben we een geheel nieuwe kantine gemaakt. We zijn erg blij met wat we in de loop van de tijd hebben neergezet. Kom eens langs, probeer het boulderen eens, het is echt vet gaaf!</h3>
        <h3>Boulderen is een zeer toegankelijke sport en door jong en oud te beoefenen. Cube Bouldergym beschikt over 600 m2 klimwand met 10 circuits van verschillende moeilijkheidsniveau's. In totaal zijn er meer dan 200 korte klimroutes (boulders). Er worden zeer regelmatig nieuwe boulders gebouwd door een vast team; Twee keer per week een half circuit van 10 boulders. Genoeg verse uitdagingen dus voor iedereen.</h3>
        <h3>Cube Bouldergym beschikt over een eigen bar en verkoopt tevens kleding van NIHIL Climbing en klimschoenen van Mad Rock. Daarnaast kun je bij de Cube terecht voor accessoires zoals sporttape, chalk(bags) en crashpads.</h3>
        <h3>Cube Bouldergym organiseert ook regionale en landelijke boulderwedstrijden en klimevenementen.</h3>
        <h3>De boulderhal is gevestigd op het voormalige Polaroid terrein in Enschede en is één van de participanten van The Performance Factory. Binnen het concept van The Performance Factory wordt ruimte geboden aan talentontwikkeling op het gebied van onderwijs, kunst, cultuur en sport.</h3>
